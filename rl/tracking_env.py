_XML_PATH = None  # set by load_mjx_model


def _find_xml_path() -> str:
    """Find the go2-flat.xml generated by gym_quadruped."""
    import gym_quadruped
    from pathlib import Path
    pkg = Path(gym_quadruped.__file__).parent
    xml = pkg / "utils" / "mujoco" / "assets" / "go2-flat.xml"
    if xml.exists():
        return str(xml)
    # Fallback: generate it via QuadrupedEnv
    from gym_quadruped.quadruped_env import QuadrupedEnv
    env = QuadrupedEnv(robot="go2", scene="flat", sim_dt=0.001)
    env.close()
    assert xml.exists(), f"go2-flat.xml not found at {xml}"
    return str(xml)


def load_mjx_model(sim_dt: float = 0.001) -> tuple[mujoco.MjModel, Any]:
    """Load Go2 model, convert cylinders→capsules for MJX, return (mj_model, mjx_model)."""
    global _XML_PATH
    if _XML_PATH is None:
        _XML_PATH = _find_xml_path()

    mj_model = mujoco.MjModel.from_xml_path(_XML_PATH)
    mj_model.opt.timestep = sim_dt
    # MJX solver iterations: 6 is sufficient, higher values are catastrophically slow
    mj_model.opt.iterations = 6
    mj_model.opt.ls_iterations = 6
    # Increase contact damping to reduce landing bounce (o_solref = [timeconst, damping])
    mj_model.opt.o_solref[1] = 1.5

    # Convert collision cylinders to capsules (MJX doesn't support cylinder-box)
    for i in range(mj_model.ngeom):
        if mj_model.geom_contype[i] > 0 and mj_model.geom_type[i] == 5:
            mj_model.geom_type[i] = 3  # capsule

    mjx_model = mjx.put_model(mj_model)
    return mj_model, mjx_model


def _get_ground_geom_id(mj_model: mujoco.MjModel) -> int:
    """Get the ground geom ID (floor)."""
    gid = mujoco.mj_name2id(mj_model, mujoco.mjtObj.mjOBJ_GEOM, "floor")
    return int(gid)


def _get_foot_geom_ids(mj_model: mujoco.MjModel) -> np.ndarray:
    """Get foot geom IDs for FL, FR, RL, RR. Returns (4,) int32."""
    return np.array([
        mujoco.mj_name2id(mj_model, mujoco.mjtObj.mjOBJ_GEOM, name)
        for name in FOOT_GEOM_NAMES
    ], dtype=np.int32)


