"""Go2 trajectory tracking environment — JAX/MJX vectorized.

Pure JAX functions for reset, step, reward, termination. All JIT-compatible.
Replaces the CPU gymnasium env with GPU-accelerated MJX physics.

Observation (39D): body_pos(3) + body_quat(4) + joints(12) + body_vel(6) + joint_vel(12) + phase(2)
Action (12D): residual joint position corrections
Actuation: PD controller + J^T·F feedforward
Reward: Additive weighted sum of 5 Gaussian terms (OPT-Mimic Eq. 15-16)
"""

from __future__ import annotations

from typing import Any, NamedTuple

import jax
import jax.numpy as jnp
import mujoco
import mujoco.mjx as mjx
import numpy as np

from .reference import ReferenceTrajectory


# ---------------------------------------------------------------------------
# Constants (same as original CPU env, adapted for Go2)
# ---------------------------------------------------------------------------
KP = 25.0
KD = 1.5
# Per-joint torque limits from Go2 actuator ctrlrange (hip/thigh=23.7, calf=45.43)
TORQUE_LIMITS = np.array([
    23.7, 23.7, 45.43,  # FL: hip, thigh, calf
    23.7, 23.7, 45.43,  # FR
    23.7, 23.7, 45.43,  # RL
    23.7, 23.7, 45.43,  # RR
], dtype=np.float32)
ACTION_LIMIT = 0.2

SIGMA_POS = 0.10
SIGMA_ORI = 0.25
SIGMA_JOINT = 0.5
SIGMA_SMOOTH = 1.0
SIGMA_TORQUE = 40.0

W_POS = 0.3
W_ORI = 0.3
W_JOINT = 0.2
W_SMOOTH = 0.1
W_TORQUE = 0.1

TERM_MULTIPLIER = 2.5
OBS_DIM = 39
N_SUBSTEPS = 20  # 50Hz policy / 1kHz physics
CONTACT_GRACE_WINDOW = 12  # 240ms at 50Hz
FOOT_GEOM_NAMES = ("FL", "FR", "RL", "RR")


# ---------------------------------------------------------------------------
# Data containers
# ---------------------------------------------------------------------------

class EnvState(NamedTuple):
    mjx_data: Any  # mjx.Data
    phase: jnp.ndarray  # int32 scalar
    prev_action: jnp.ndarray  # (12,)
    last_torque: jnp.ndarray  # (12,)
    first_step: jnp.ndarray  # bool scalar
    joint_offset: jnp.ndarray  # (12,)
    torque_scale: jnp.ndarray  # float32 scalar
    done: jnp.ndarray  # bool scalar


class RefData(NamedTuple):
    body_pos: jnp.ndarray  # (N+1, 3)
    body_quat: jnp.ndarray  # (N+1, 4)
    joint_pos: jnp.ndarray  # (N+1, 12)
    joint_vel: jnp.ndarray  # (N, 12)
    body_vel: jnp.ndarray  # (N+1, 3)
    body_ang_vel: jnp.ndarray  # (N+1, 3)
    ff_torques: jnp.ndarray  # (N, 12)
    max_phase: jnp.ndarray  # int32 scalar
    contact_sequence: jnp.ndarray  # (4, N) — 1=stance, 0=swing, -1=unavailable
    ground_geom_id: jnp.ndarray  # int32 scalar
    foot_geom_ids: jnp.ndarray  # (4,) int32
    near_transition: jnp.ndarray  # (4, N) — 1.0 if near contact transition


