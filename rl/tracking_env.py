def make_ref_data(ref: ReferenceTrajectory, mj_model: mujoco.MjModel) -> RefData:
    """Convert CPU ReferenceTrajectory to JAX RefData arrays."""
    N = ref.max_phase
    body_pos = np.zeros((N + 1, 3))
    body_quat = np.zeros((N + 1, 4))
    joint_pos = np.zeros((N + 1, 12))
    body_vel = np.zeros((N + 1, 3))
    body_ang_vel = np.zeros((N + 1, 3))

    for k in range(N + 1):
        body_pos[k] = ref.get_body_pos(k)
        body_quat[k] = ref.get_body_quat(k)
        joint_pos[k] = ref.get_joint_pos(k)
        body_vel[k] = ref.get_body_vel(k)
        body_ang_vel[k] = ref.get_body_ang_vel(k)

    joint_vel = np.zeros((N, 12))
    ff_torques = np.zeros((N, 12))
    for k in range(N):
        joint_vel[k] = ref.get_joint_vel(k)
        ff_torques[k] = ref.get_feedforward_torque(k)

    # Contact sequence: (4, N) with -1 sentinel when unavailable
    if ref.contact_sequence is not None:
        contact_sequence = ref.contact_sequence[:, :N].astype(np.float32)
    else:
        contact_sequence = -np.ones((4, N), dtype=np.float32)

    # Geom IDs for contact termination
    ground_geom_id = _get_ground_geom_id(mj_model)
    foot_geom_ids = _get_foot_geom_ids(mj_model)

    # Precompute near-transition mask for contact grace window (120ms)
    near_transition = np.ones((4, N), dtype=np.float32)  # default: all grace
    if ref.contact_sequence is not None:
        near_transition = np.zeros((4, N), dtype=np.float32)
        for foot in range(4):
            for k in range(N):
                current = ref.contact_sequence[foot, k]
                lo = max(0, k - CONTACT_GRACE_WINDOW)
                hi = min(N - 1, k + CONTACT_GRACE_WINDOW)
                if np.any(ref.contact_sequence[foot, lo:hi + 1] != current):
                    near_transition[foot, k] = 1.0

    return RefData(
        body_pos=jnp.array(body_pos),
        body_quat=jnp.array(body_quat),
        joint_pos=jnp.array(joint_pos),
        joint_vel=jnp.array(joint_vel),
        body_vel=jnp.array(body_vel),
        body_ang_vel=jnp.array(body_ang_vel),
        ff_torques=jnp.array(ff_torques),
        max_phase=jnp.int32(N),
        contact_sequence=jnp.array(contact_sequence),
        ground_geom_id=jnp.int32(ground_geom_id),
        foot_geom_ids=jnp.array(foot_geom_ids),
        near_transition=jnp.array(near_transition),
    )


# ---------------------------------------------------------------------------
# Core env functions (all JIT-compatible)
# ---------------------------------------------------------------------------

def _clamp_phase(phase: jnp.ndarray, max_phase: jnp.ndarray) -> jnp.ndarray:
    return jnp.clip(phase, 0, max_phase - 1)


def _quat_error(q_ref: jnp.ndarray, q_actual: jnp.ndarray) -> jnp.ndarray:
    """Quaternion error: vector part of q_ref * q_actual^{-1}. Returns (3,)."""
    q_inv = jnp.array([q_actual[0], -q_actual[1], -q_actual[2], -q_actual[3]])
    w1, x1, y1, z1 = q_ref[0], q_ref[1], q_ref[2], q_ref[3]
    w2, x2, y2, z2 = q_inv[0], q_inv[1], q_inv[2], q_inv[3]
    return jnp.array([
        w1 * x2 + x1 * w2 + y1 * z2 - z1 * y2,
        w1 * y2 - x1 * z2 + y1 * w2 + z1 * x2,
        w1 * z2 + x1 * y2 - y1 * x2 + z1 * w2,
    ])


def get_obs(state: EnvState, ref_data: RefData) -> jnp.ndarray:
    """Build 39D observation: sensor(37) + phase(2)."""
    data = state.mjx_data
    phase = _clamp_phase(state.phase, ref_data.max_phase)

    body_pos = data.qpos[0:3]
    body_quat = data.qpos[3:7]
    joint_pos = data.qpos[7:19] + state.joint_offset
    body_vel = data.qvel[0:3]
    body_ang_vel = data.qvel[3:6]
    joint_vel = data.qvel[6:18]

    angle = 2.0 * jnp.pi * phase / ref_data.max_phase
    phase_enc = jnp.array([jnp.cos(angle), jnp.sin(angle)])

    return jnp.concatenate([
        body_pos, body_quat, joint_pos,
        body_vel, body_ang_vel, joint_vel,
        phase_enc,
    ])


def compute_reward(
    state: EnvState, action: jnp.ndarray, ref_data: RefData
) -> tuple[jnp.ndarray, dict[str, jnp.ndarray]]:
    """Additive weighted sum of 5 Gaussian terms (OPT-Mimic Eq. 15)."""
    data = state.mjx_data
    phase = _clamp_phase(state.phase, ref_data.max_phase)

    ref_pos = ref_data.body_pos[phase]
    ref_quat = ref_data.body_quat[phase]
    ref_joint = ref_data.joint_pos[phase]

    pos_err_sq = jnp.sum((ref_pos - data.qpos[0:3]) ** 2)
    r_pos = jnp.exp(-pos_err_sq / (2.0 * SIGMA_POS**2))

    ori_err = _quat_error(ref_quat, data.qpos[3:7])
    ori_err_sq = jnp.sum(ori_err**2)
    r_ori = jnp.exp(-ori_err_sq / (2.0 * SIGMA_ORI**2))

    joint_err_sq = jnp.sum((ref_joint - data.qpos[7:19]) ** 2)
    r_joint = jnp.exp(-joint_err_sq / (2.0 * SIGMA_JOINT**2))

    rate_sq = jnp.sum((action - state.prev_action) ** 2)
    r_smooth = jnp.exp(-rate_sq / (2.0 * SIGMA_SMOOTH**2))

    max_torque = jnp.max(jnp.abs(state.last_torque))
    r_torque = jnp.exp(-(max_torque**2) / (2.0 * SIGMA_TORQUE**2))

    total = (
        W_POS * r_pos + W_ORI * r_ori + W_JOINT * r_joint
        + W_SMOOTH * r_smooth + W_TORQUE * r_torque
    )

    info = {
        "pos_error": jnp.sqrt(pos_err_sq),
        "ori_error": jnp.sqrt(ori_err_sq),
        "joint_error": jnp.sqrt(joint_err_sq),
        "action_rate": jnp.sqrt(rate_sq),
        "max_torque": max_torque,
        "rw_pos": r_pos,
        "rw_ori": r_ori,
        "rw_joint": r_joint,
        "rw_smooth": r_smooth,
        "rw_torque": r_torque,
    }
    return total, info


def check_termination(
    state: EnvState, reward_info: dict[str, jnp.ndarray],
    ref_data: RefData,
) -> tuple[jnp.ndarray, jnp.ndarray]:
    """Early termination per OPT-Mimic III-C.4:
    1. Any tracking error exceeds 2.5x sigma.
    2. Non-foot body touches ground.
    3. Foot contact mismatch with reference (120ms grace window).

    Returns (terminated: bool, reason: int32).
    Reason codes: 0=none, 1=threshold, 2=body_contact, 3=contact_mismatch, 4=nan.
    """
    thresh_pos = TERM_MULTIPLIER * SIGMA_POS
    thresh_ori = TERM_MULTIPLIER * SIGMA_ORI
    thresh_joint = TERM_MULTIPLIER * SIGMA_JOINT
    thresh_smooth = TERM_MULTIPLIER * SIGMA_SMOOTH
    thresh_torque = TERM_MULTIPLIER * SIGMA_TORQUE

    reason = jnp.int32(0)

    # Condition 1: tracking error thresholds
    thresh_hit = (
        (reward_info["pos_error"] > thresh_pos)
        | (reward_info["ori_error"] > thresh_ori)
        | (reward_info["joint_error"] > thresh_joint)
        | ((~state.first_step) & (reward_info["action_rate"] > thresh_smooth))
        | (reward_info["max_torque"] > thresh_torque)
    )
    reason = jnp.where(thresh_hit, jnp.int32(1), reason)

    # Condition 2: non-foot body ground contact
    contact = state.mjx_data.contact
    active = contact.dist <= 0  # (ncon,)
    g1_ground = contact.geom1 == ref_data.ground_geom_id
    g2_ground = contact.geom2 == ref_data.ground_geom_id
    involves_ground = (g1_ground | g2_ground) & active
    other_geom = jnp.where(g1_ground, contact.geom2, contact.geom1)
    is_foot = jnp.any(
        other_geom[:, None] == ref_data.foot_geom_ids[None, :], axis=1
    )  # (ncon,)
    non_foot_ground = jnp.any(involves_ground & ~is_foot)
    reason = jnp.where(non_foot_ground & (reason == 0), jnp.int32(2), reason)

    # Condition 3: foot contact mismatch with grace window
    phase = _clamp_phase(state.phase, ref_data.max_phase)
    ref_contact = ref_data.contact_sequence[:, phase]  # (4,)
    has_contact_info = ref_contact[0] >= 0  # -1 sentinel = unavailable
    foot_match = other_geom[:, None] == ref_data.foot_geom_ids[None, :]  # (ncon, 4)
    actual_contact = jnp.any(foot_match & involves_ground[:, None], axis=0)  # (4,)
    expected_contact = ref_contact > 0.5  # (4,) bool
    grace = ref_data.near_transition[:, phase]  # (4,) — 1.0 if near transition
    mismatch = (actual_contact != expected_contact) & (grace < 0.5)
    contact_mismatch = has_contact_info & jnp.any(mismatch)
    reason = jnp.where(contact_mismatch & (reason == 0), jnp.int32(3), reason)

    # NaN check (physics divergence)
    has_nan = jnp.any(jnp.isnan(state.mjx_data.qpos))
    reason = jnp.where(has_nan & (reason == 0), jnp.int32(4), reason)

    terminated = reason > 0
    return terminated, reason


def reset(
    rng: jnp.ndarray, mjx_model: Any, mj_model: mujoco.MjModel,
    ref_data: RefData, randomize: bool = True,
) -> EnvState:
    """Reset environment to reference state with domain randomization."""
    rng, rng_phase, rng_offset, rng_torque = jax.random.split(rng, 4)

    start_phase = jnp.where(
        randomize,
        jax.random.randint(rng_phase, (), 0, ref_data.max_phase),
        jnp.int32(0),
    )

    joint_offset = jnp.where(
        randomize,
        jax.random.normal(rng_offset, (12,)) * 0.02,
        jnp.zeros(12),
    )

    torque_scale = jnp.where(
        randomize,
        jnp.clip(1.0 + jax.random.normal(rng_torque, ()) * 0.1, 0.5, 1.5),
        jnp.float32(1.0),
    )

    phase = _clamp_phase(start_phase, ref_data.max_phase)

    # Build initial qpos/qvel from reference
    qpos = jnp.zeros(19)
    qpos = qpos.at[0:3].set(ref_data.body_pos[phase])
    qpos = qpos.at[3:7].set(ref_data.body_quat[phase])
    qpos = qpos.at[7:19].set(ref_data.joint_pos[phase])

    qvel = jnp.zeros(18)
    qvel = qvel.at[0:3].set(ref_data.body_vel[phase])
    qvel = qvel.at[3:6].set(ref_data.body_ang_vel[phase])
    vel_phase = _clamp_phase(start_phase, ref_data.max_phase)
    qvel = qvel.at[6:18].set(ref_data.joint_vel[vel_phase])

    # Create MJX data with reference state
    mj_data = mujoco.MjData(mj_model)
    mjx_data = mjx.put_data(mj_model, mj_data)
    mjx_data = mjx_data.replace(qpos=qpos, qvel=qvel)
    mjx_data = mjx.forward(mjx_model, mjx_data)

    return EnvState(
        mjx_data=mjx_data,
        phase=start_phase,
        prev_action=jnp.zeros(12),
        last_torque=jnp.zeros(12),
        first_step=jnp.bool_(True),
        joint_offset=joint_offset,
        torque_scale=torque_scale,
        done=jnp.bool_(False),
    )


def reset_fast(
    rng: jnp.ndarray, mjx_data_template: Any,
    ref_data: RefData, randomize: bool = True,
) -> EnvState:
    """Fast reset using existing mjx_data (no mj_model needed). For auto-reset in scan."""
    rng, rng_phase, rng_offset, rng_torque = jax.random.split(rng, 4)

    start_phase = jnp.where(
        randomize,
        jax.random.randint(rng_phase, (), 0, ref_data.max_phase),
        jnp.int32(0),
    )

    joint_offset = jnp.where(
        randomize,
        jax.random.normal(rng_offset, (12,)) * 0.02,
        jnp.zeros(12),
    )

    torque_scale = jnp.where(
        randomize,
        jnp.clip(1.0 + jax.random.normal(rng_torque, ()) * 0.1, 0.5, 1.5),
        jnp.float32(1.0),
    )

    phase = _clamp_phase(start_phase, ref_data.max_phase)

    qpos = jnp.zeros(19)
    qpos = qpos.at[0:3].set(ref_data.body_pos[phase])
    qpos = qpos.at[3:7].set(ref_data.body_quat[phase])
    qpos = qpos.at[7:19].set(ref_data.joint_pos[phase])

    qvel = jnp.zeros(18)
    qvel = qvel.at[0:3].set(ref_data.body_vel[phase])
    qvel = qvel.at[3:6].set(ref_data.body_ang_vel[phase])
    vel_phase = _clamp_phase(start_phase, ref_data.max_phase)
    qvel = qvel.at[6:18].set(ref_data.joint_vel[vel_phase])

    new_data = mjx_data_template.replace(
        qpos=qpos, qvel=qvel,
        ctrl=jnp.zeros(12),
        time=jnp.float32(0.0),
    )
    # Skip mjx.forward here — the next mjx.step in the scan body will
    # compute derived quantities. get_obs only needs qpos/qvel which are set.

    return EnvState(
        mjx_data=new_data,
        phase=start_phase,
        prev_action=jnp.zeros(12),
        last_torque=jnp.zeros(12),
        first_step=jnp.bool_(True),
        joint_offset=joint_offset,
        torque_scale=torque_scale,
        done=jnp.bool_(False),
    )


def step(
    state: EnvState, action: jnp.ndarray, mjx_model: Any,
    ref_data: RefData,
) -> tuple[EnvState, jnp.ndarray, jnp.ndarray, jnp.ndarray, dict]:
    """Step environment: apply PD+ff torque, substep physics, compute reward/done.

    Returns (new_state, obs, reward, done, reward_info).
    """
    # Scale action
    action = action * ACTION_LIMIT

    phase = _clamp_phase(state.phase, ref_data.max_phase)

    ref_joint_pos = ref_data.joint_pos[phase]
    ref_joint_vel = ref_data.joint_vel[phase]
    ff_torque = ref_data.ff_torques[phase]

    actual_joint_pos = state.mjx_data.qpos[7:19]
    actual_joint_vel = state.mjx_data.qvel[6:18]

    target_pos = ref_joint_pos + action + state.joint_offset
    torque = (
        KP * (target_pos - actual_joint_pos)
        + KD * (ref_joint_vel - actual_joint_vel)
        + ff_torque
    )
    torque = jnp.clip(torque, -jnp.array(TORQUE_LIMITS), jnp.array(TORQUE_LIMITS))
    torque = torque * state.torque_scale

    # Substep physics
    data = state.mjx_data.replace(ctrl=torque)

    def substep_fn(data, _):
        return mjx.step(mjx_model, data), None

    data, _ = jax.lax.scan(substep_fn, data, None, length=N_SUBSTEPS)

    new_state = EnvState(
        mjx_data=data,
        phase=state.phase + 1,
        prev_action=action,
        last_torque=torque,
        first_step=jnp.bool_(False),
        joint_offset=state.joint_offset,
        torque_scale=state.torque_scale,
        done=state.done,
    )

    reward, reward_info = compute_reward(new_state, action, ref_data)
    terminated, term_reason = check_termination(new_state, reward_info, ref_data)
    truncated = new_state.phase >= ref_data.max_phase

    done = terminated | truncated
    # Truncation = reason 5 (only if not already terminated)
    term_reason = jnp.where(truncated & ~terminated, jnp.int32(5), term_reason)
    reward_info["term_reason"] = term_reason.astype(jnp.float32)

    # Force reward to 0 if NaN
    reward = jnp.where(jnp.isnan(reward), 0.0, reward)

    new_state = new_state._replace(done=done)
    obs = get_obs(new_state, ref_data)

    # Replace NaN obs with 0
    obs = jnp.where(jnp.isnan(obs), 0.0, obs)

    return new_state, obs, reward, done, reward_info


# ---------------------------------------------------------------------------
# Batched (vmapped) env functions
# ---------------------------------------------------------------------------

def make_batched_fns(
    mjx_model: Any, ref_data: RefData,
):
    """Create batched (vmapped) reset and step functions.

    Returns (batched_reset, batched_step, batched_get_obs).
    """
    def _reset_single(rng, mjx_data_template):
        return reset_fast(rng, mjx_data_template, ref_data, randomize=True)

    def _step_single(state, action):
        return step(state, action, mjx_model, ref_data)

    def _get_obs_single(state):
        return get_obs(state, ref_data)

    batched_step = jax.vmap(_step_single)
    batched_get_obs = jax.vmap(_get_obs_single)

    return _reset_single, batched_step, batched_get_obs


